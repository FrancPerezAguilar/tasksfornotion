export const colorTags = (color) => {
    switch(color) {
        case "orange":
            return "#fadec9";
        
        case "green":
            return "#dbeddb";

        case "red":
            return "#ffe2dd";

        case "blue":
            return "#d3e5ef";

        case "purple":
            return "#e8deee";

        default:
            return "#3c3c3c";
    }
}